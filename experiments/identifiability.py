"""
BEIRAN-STYLE IDENTIFIABILITY OF A DELAY-COUPLED RNN (the re-sculpting-proof spine).

Template: Beiran & Litwin-Kumar, "Prediction of neural activity in connectome-
constrained recurrent networks," Nature Neuroscience 28(12):2561-2574 (2025),
DOI 10.1038/s41593-025-02080-4 (PMC11160579; bioRxiv 2024.02.22.581667). Their
result: a *connectome* (which units connect) is often insufficient to constrain
network dynamics -- there are ~N^2 free synaptic strengths -- but a handful of
recordings collapses the degeneracy; once you record M* (order N) units you can
predict the activity of *held-out* units the recordings never touched. The
connectome is a structural prior; the recordings supply what it underdetermines.

WE APPLY THIS TO DELAYS. A spatial delay-coupled RNN has a DELAY CONNECTOME
tau_ij = round(d_ij / v). The teacher's recurrent weights are themselves a smooth
function of the SAME distance (a "connectome" prior: nearby units couple more) --
so the whole network is generated by a low-dimensional object: a coordinate cloud,
a conduction velocity, and a distance->weight kernel. The question:

    Does the METRIC structure of the delays (tau and W both organized by the same
    distance) let a student IDENTIFY the network -- and PREDICT held-out units --
    from FEWER recordings than a student given (b) entry-shuffled delays of the
    same histogram, or (c) no-delay / weight-only?

Mechanism. The student is told the delay matrix tau_s (metric / shuffled / none)
and the distance matrix, and learns a SHARED distance->weight kernel from the
recorded channels. With the METRIC tau_s, the lag at which each weight acts is
consistent across all unit pairs at a given distance, so a kernel fit on RECORDED
rows transfers to HELD-OUT rows: identifiability. With the SHUFFLED tau_s, the
distance->lag correspondence is destroyed (a pair at distance d can have any lag),
so no shared kernel can place the right weight at the right lag for held-out units
-- recordings don't transfer. Prediction: METRIC predicts held-out units with
FEWER recordings, and the gap SHRINKS as K -> N (when recordings alone pin every
row, the prior stops mattering -- exactly Beiran's M*-recordings-suffice crossover).

------------------------------------------------------------------------------
WHY THIS IS RE-SCULPTING-PROOF (the project's feasibility filter)
------------------------------------------------------------------------------
Every prior FUNCTIONAL probe died because a fully trainable recurrent matrix can
RE-SCULPT a scrambled-delay code back to matched task accuracy and generalization.
Re-sculpting cannot save the shuffle here, for two stacked reasons:

  1. HELD-OUT UNITS are never in the student's loss. The student fits a kernel to
     reproduce the teacher on K RECORDED units; it is SCORED on the N-K units it
     never recorded and never trained on. Re-sculpting can refit only the recorded
     channels; it cannot manufacture correct predictions for held-out dynamics it
     was never shown. (This is exactly why Beiran's held-out-neuron prediction --
     not training loss -- is the decisive readout.)

  2. The DELAY GEOMETRY is FROZEN and the input map is a FROZEN IDENTITY (each
     recorded channel drives its own unit), as in localization.py. The student may
     not change tau_s, cannot relabel units (a permutation a readout would absorb),
     and cannot re-derive a metric the shuffle lacks. The ONLY thing that differs
     across conditions is the structural prior baked into tau_s, and the only thing
     that exploits it is identifiability -- not re-sculpting.

We additionally VERIFY non-circularity: (a) observed-channel fit is matched across
conditions at small K (the metric must not win merely by fitting RECORDED units
better -- if anything the shuffle has more freedom to overfit them); (b) a
consistently RELABELED metric (index sanity) behaves like the metric (gap ~ 0),
proving it is metric CONSISTENCY, not "any delay change", that the shuffle breaks.

------------------------------------------------------------------------------
DESIGN (teacher -> student, three delay conditions, sweep over K recordings)
------------------------------------------------------------------------------
TEACHER  delay-coupled rate RNN. Coordinates ~ U[0,1]^2; tau* = round(d/v);
         recurrent weight W*_ij = gain_ij * kernel(d_ij), kernel a smooth decaying
         function of distance (the connectome prior), gain_ij a fixed low-variance
         random sign/scale so the map is not perfectly symmetric. Frozen-identity
         input drives each unit. Teacher is FIXED (it is the ground truth) and we
         read off its rate trajectories.

STUDENT  same architecture, same frozen-identity input, given tau_s in
         {metric, shuffle, none}. It learns a SHARED distance->weight kernel
         (a small MLP on d_ij, applied at the lag dictated by tau_s) plus a
         per-unit gain on RECORDED units only. The kernel is shared across all
         pairs, so fitting recorded rows constrains held-out rows THROUGH the
         metric. Only the kernel + recorded gains train; tau_s and W_in frozen.

OBSERVE  first K of a fixed random unit order are "recorded". Identify by
         teacher-forcing TRUE history on all channels and fitting one-step-ahead
         targets on the K recorded channels. SCORE on the N-K held-out channels by
         running the identified net FREE on held-out units (recorded units still
         teacher-forced) and measuring held-out prediction R^2 / RMSE.

SWEEP    K = small ... N. EXPECTED metric >> shuffle, none at small K; converge as
         K -> N.

USAGE
  smoke:  python experiments/identifiability.py --smoke
  full:   python experiments/identifiability.py --N 80 --seeds 5 \
              --K_list 4,8,16,32,64,80 --student_steps 1200 --index_sanity
Writes results/experiments/identifiability.json.
"""
from __future__ import annotations

import argparse
import json
import math
import os
import time

import numpy as np
import torch
import torch.nn as nn


# --------------------------------------------------------------------------- #
def pick_device(prefer: str = "auto") -> str:
    if prefer != "auto":
        return prefer
    if torch.cuda.is_available():
        return "cuda"
    if torch.backends.mps.is_available():
        return "mps"
    return "cpu"


# --------------------------------------------------------------------------- #
# geometry + delays
# --------------------------------------------------------------------------- #
def positions(N: int, seed: int = 0, dim: int = 2) -> torch.Tensor:
    g = torch.Generator().manual_seed(seed)
    return torch.rand(N, dim, generator=g)


def metric_delays(pos: torch.Tensor, v: float, maxd: int) -> torch.Tensor:
    d = torch.cdist(pos, pos)
    tau = torch.round(d / v).clamp(1, maxd).long()
    tau.fill_diagonal_(1)
    return tau


def make_student_delays(tau_metric: torch.Tensor, condition: str, seed: int):
    """Return (tau_for_student, relabel_perm_or_None) for each condition."""
    tau = tau_metric
    N = tau.shape[0]
    if condition == "metric":
        return tau.clone(), None
    if condition == "none":
        return torch.ones_like(tau), None
    if condition == "shuffle":
        g = torch.Generator().manual_seed(1000 + seed)
        iu = torch.triu_indices(N, N, offset=1)
        vals = tau[iu[0], iu[1]]
        vals = vals[torch.randperm(vals.numel(), generator=g)]
        out = torch.ones_like(tau)
        out[iu[0], iu[1]] = vals
        out[iu[1], iu[0]] = vals
        out.fill_diagonal_(1)
        return out, None
    if condition == "index":
        g = torch.Generator().manual_seed(7000 + seed)
        perm = torch.randperm(N, generator=g)
        return tau[perm][:, perm].clone(), perm
    raise ValueError(condition)


# --------------------------------------------------------------------------- #
# TEACHER: distance-structured, delay-coupled rate RNN (the ground truth)
# --------------------------------------------------------------------------- #
def teacher_weight(pos: torch.Tensor, seed: int, g_rec: float = 1.4) -> torch.Tensor:
    """W*_ij = gain_ij * exp(-d_ij / ell) -- the connectome prior: coupling is a
    smooth decaying function of distance, with a fixed low-variance random gain so
    W is not perfectly symmetric. This makes the whole teacher generatable from a
    low-dim object (coords + velocity + kernel), so a few recordings can in
    principle pin the held-out rows -- the Beiran setup."""
    N = pos.shape[0]
    d = torch.cdist(pos, pos)
    ell = 0.35
    kernel = torch.exp(-d / ell)
    g = torch.Generator().manual_seed(seed)
    gain = 1.0 + 0.3 * torch.randn(N, N, generator=g)
    W = g_rec / math.sqrt(N) * gain * kernel
    W.fill_diagonal_(0.0)
    return W


# --------------------------------------------------------------------------- #
# delay-coupled rate RNN core (used by teacher with a fixed W, and by the
# rollout of a student whose W is produced by a distance kernel each call)
# --------------------------------------------------------------------------- #
def lag_masks(tau: torch.Tensor):
    lags = sorted(int(x) for x in torch.unique(tau).tolist())
    masks = torch.stack([(tau == d).float() for d in lags])  # (L, N, N)
    return lags, masks


def rollout(W, b, tau_lags, tau_masks, maxd, alpha, X,
            teacher_force=None, tf_mask=None):
    """h-rate rollout. rec_i[t] = sum_j W_ij tanh(h_j[t - tau_ij]); grouped by lag.

    X: (T, B, N). teacher_force: (T,B,N) true rates; tf_mask: (N,) bool channels
    to clamp to the teacher value each step. Returns rates (T, B, N) = tanh(state).
    """
    T, B, N = X.shape
    dev = X.device
    hist = torch.zeros(maxd + 1, B, N, device=dev)
    out = []
    phi = torch.tanh
    Wt_groups = [(d, (W * tau_masks[li]).t()) for li, d in enumerate(tau_lags)]
    for t in range(T):
        rec = torch.zeros(B, N, device=dev)
        for d, WtG in Wt_groups:
            rec = rec + phi(hist[d]) @ WtG
        pre = rec + X[t] + b
        state = (1 - alpha) * hist[1] + alpha * pre
        r = phi(state)
        if teacher_force is not None and tf_mask is not None:
            r = torch.where(tf_mask.view(1, N), teacher_force[t], r)
        hist = torch.roll(hist, 1, 0)
        hist[1] = r
        out.append(r)
    return torch.stack(out, 0)


# --------------------------------------------------------------------------- #
# STUDENT kernel: a shared distance->weight function, evaluated per pair.
# This is the structural prior that lets recorded rows constrain held-out rows.
# --------------------------------------------------------------------------- #
class KernelStudent(nn.Module):
    """Produces a recurrent weight W_ij = mlp(d_ij) * row_gain_i, shared across all
    pairs (so it generalizes from recorded to held-out rows). The lag at which each
    W_ij acts is set by the FROZEN tau_s. Only the MLP and the recorded row-gains
    train. Held-out row-gains are fixed at 1 (their dynamics must be predicted from
    the shared kernel alone -- the real test of identifiability transfer)."""

    def __init__(self, N, dist, scale=1.0, hidden=16, seed=0):
        super().__init__()
        self.N = N
        self.register_buffer("dist", dist)               # (N, N)
        g = torch.Generator().manual_seed(seed)
        self.net = nn.Sequential(
            nn.Linear(1, hidden), nn.Tanh(),
            nn.Linear(hidden, hidden), nn.Tanh(),
            nn.Linear(hidden, 1),
        )
        for m in self.net:
            if isinstance(m, nn.Linear):
                with torch.no_grad():
                    m.weight.normal_(0, 0.5, generator=g)
                    m.bias.zero_()
        self.row_gain = nn.Parameter(torch.ones(N))      # per-target-unit scale
        self.b = nn.Parameter(torch.zeros(N))
        self.scale = scale

    def weight(self):
        d = self.dist.reshape(-1, 1)
        k = self.net(d).reshape(self.N, self.N)
        W = self.scale * k * self.row_gain.view(-1, 1)   # row i scaled by gain_i
        W = W - torch.diag_embed(torch.diagonal(W))      # zero diagonal
        return W


# --------------------------------------------------------------------------- #
def make_inputs(N, T, B, seed, device, drive=0.6, tau_in=3):
    g = torch.Generator().manual_seed(seed)
    raw = torch.randn(T, B, N, generator=g) * drive
    X = torch.zeros_like(raw)
    a = 1.0 / tau_in
    acc = torch.zeros(B, N)
    for t in range(T):
        acc = (1 - a) * acc + a * raw[t]
        X[t] = acc
    return X.to(device)


def r2_score(pred, target):
    ss_res = ((pred - target) ** 2).sum()
    ss_tot = ((target - target.mean()) ** 2).sum().clamp_min(1e-12)
    return float(1.0 - ss_res / ss_tot)


# --------------------------------------------------------------------------- #
# one (condition, K) student identify + held-out predict
# --------------------------------------------------------------------------- #
def fit_student(W_star, b_star, alpha, dist_metric, tau_s, perm,
                X_train, R_train, X_test, R_test,
                obs_idx, hidden_idx, device, steps, lr, seed, scale, maxd):
    N = W_star.shape[0]

    # index-sanity relabel: permute the unit axis of everything consistently so the
    # relabeled geometry lines up with relabeled truth (a valid alt-geometry).
    if perm is not None:
        inv = torch.argsort(perm)
        Xtr, Xte = X_train[..., perm], X_test[..., perm]
        Rtr, Rte = R_train[..., perm], R_test[..., perm]
        dist_s = dist_metric[perm][:, perm]
        obs = inv[obs_idx].to(device)
        hid = inv[hidden_idx].to(device)
    else:
        Xtr, Xte, Rtr, Rte = X_train, X_test, R_train, R_test
        dist_s = dist_metric
        obs = obs_idx.to(device)
        hid = hidden_idx.to(device)

    tau_lags, tau_masks = lag_masks(tau_s.to(device))
    tau_masks = tau_masks.to(device)

    student = KernelStudent(N, dist_s.to(device), scale=scale, seed=seed + 13).to(device)
    opt = torch.optim.Adam(student.parameters(), lr=lr)

    all_mask = torch.ones(N, dtype=torch.bool, device=device)

    # IDENTIFY: TF true history everywhere, fit one-step targets on recorded units.
    for it in range(steps):
        opt.zero_grad()
        W = student.weight()
        Rhat = rollout(W, student.b, tau_lags, tau_masks, maxd, alpha, Xtr,
                       teacher_force=Rtr, tf_mask=all_mask)
        loss = ((Rhat[..., obs] - Rtr[..., obs]) ** 2).mean()
        # tiny kernel-smoothness/L2 so the shared kernel is well-posed
        loss = loss + 1e-4 * (student.row_gain - 1.0).pow(2).mean()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(student.parameters(), 1.0)
        opt.step()

    # PREDICT held-out units: run FREE on held-out, keep recording the observed.
    tf_obs = torch.zeros(N, dtype=torch.bool, device=device)
    tf_obs[obs] = True
    with torch.no_grad():
        W = student.weight()
        Rpred = rollout(W, student.b, tau_lags, tau_masks, maxd, alpha, Xte,
                        teacher_force=Rte, tf_mask=tf_obs)
        Rone = rollout(W, student.b, tau_lags, tau_masks, maxd, alpha, Xte,
                       teacher_force=Rte, tf_mask=all_mask)  # identification residual
        w = min(maxd + 2, Rte.shape[0] // 4)
        ho_pred, ho_true = Rpred[w:, :, hid], Rte[w:, :, hid]
        ob_pred, ob_true = Rone[w:, :, obs], Rte[w:, :, obs]
        return {
            "heldout_rmse": float(((ho_pred - ho_true) ** 2).mean().sqrt()),
            "heldout_r2": r2_score(ho_pred, ho_true),
            "observed_rmse": float(((ob_pred - ob_true) ** 2).mean().sqrt()),
            "observed_r2": r2_score(ob_pred, ob_true),
        }


# --------------------------------------------------------------------------- #
def run_seed(seed, N, v, maxd, K_list, conditions, device,
             student_steps, T, B_train, B_test, g_rec, scale):
    pos = positions(N, seed=seed)
    dist = torch.cdist(pos, pos)
    tau_star = metric_delays(pos, v, maxd)
    W_star = teacher_weight(pos, seed=seed, g_rec=g_rec).to(device)
    b_star = torch.zeros(N, device=device)
    alpha = 0.25

    t_lags, t_masks = lag_masks(tau_star.to(device))
    t_masks = t_masks.to(device)
    X_train = make_inputs(N, T, B_train, seed=100 + seed, device=device)
    X_test = make_inputs(N, T, B_test, seed=900 + seed, device=device)
    with torch.no_grad():
        R_train = rollout(W_star, b_star, t_lags, t_masks, maxd, alpha, X_train)
        R_test = rollout(W_star, b_star, t_lags, t_masks, maxd, alpha, X_test)

    g = torch.Generator().manual_seed(555 + seed)
    order = torch.randperm(N, generator=g)

    seed_res = {c: {} for c in conditions}
    for cond in conditions:
        tau_s, perm = make_student_delays(tau_star, cond, seed)
        for K in K_list:
            obs_idx = order[:K]
            hidden_idx = order[K:]
            if len(hidden_idx) == 0:
                m = fit_student(W_star, b_star, alpha, dist, tau_s, perm,
                                X_train, R_train, X_test, R_test,
                                obs_idx, order[:1], device, student_steps,
                                3e-3, seed, scale, maxd)
                m["heldout_rmse"] = float("nan"); m["heldout_r2"] = float("nan")
            else:
                m = fit_student(W_star, b_star, alpha, dist, tau_s, perm,
                                X_train, R_train, X_test, R_test,
                                obs_idx, hidden_idx, device, student_steps,
                                3e-3, seed, scale, maxd)
            seed_res[cond][K] = m
            print(f"    seed {seed} {cond:8s} K={K:3d} "
                  f"heldout_r2={m['heldout_r2']:+.3f} "
                  f"rmse={m['heldout_rmse']:.4f} obs_r2={m['observed_r2']:+.3f}",
                  flush=True)
    return seed_res


# --------------------------------------------------------------------------- #
def aggregate(rows, K_list, conditions):
    # K == N has no held-out units (held-out R^2 is NaN by design); nanmean over
    # an all-NaN slice warns harmlessly -- silence it.
    import warnings
    warnings.filterwarnings("ignore", message="Mean of empty slice")
    warnings.filterwarnings("ignore", message="Degrees of freedom <= 0")

    def collect(cond, K, key):
        return np.array([r[cond][K][key] for r in rows], dtype=float)

    agg = {c: {} for c in conditions}
    for cond in conditions:
        for K in K_list:
            ho = collect(cond, K, "heldout_r2")
            hor = collect(cond, K, "heldout_rmse")
            ob = collect(cond, K, "observed_r2")
            agg[cond][K] = {
                "heldout_r2_mean": float(np.nanmean(ho)),
                "heldout_r2_sd": float(np.nanstd(ho)),
                "heldout_rmse_mean": float(np.nanmean(hor)),
                "observed_r2_mean": float(np.nanmean(ob)),
            }
    gaps = {}
    if "metric" in conditions and "shuffle" in conditions:
        for K in K_list:
            d = collect("metric", K, "heldout_r2") - collect("shuffle", K, "heldout_r2")
            d = d[~np.isnan(d)]
            if d.size == 0:
                continue
            m, sd = float(d.mean()), float(d.std() + 1e-9)
            gaps[K] = {"metric_minus_shuffle_r2": m, "sd": sd,
                       "t": m / (sd / math.sqrt(len(d))),
                       "wins": int((d > 0).sum()), "n": int(len(d))}
    return agg, gaps


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--N", type=int, default=80)
    ap.add_argument("--seeds", type=int, default=5)
    ap.add_argument("--v", type=float, default=0.12)
    ap.add_argument("--maxd", type=int, default=16)
    ap.add_argument("--g_rec", type=float, default=1.4)
    ap.add_argument("--scale", type=float, default=1.0)
    ap.add_argument("--K_list", type=str, default="4,8,16,32,64,80")
    ap.add_argument("--student_steps", type=int, default=1200)
    ap.add_argument("--T", type=int, default=60)
    ap.add_argument("--B_train", type=int, default=16)
    ap.add_argument("--B_test", type=int, default=32)
    ap.add_argument("--device", type=str, default="auto")
    ap.add_argument("--index_sanity", action="store_true")
    ap.add_argument("--out", type=str, default="results/experiments/identifiability.json")
    ap.add_argument("--smoke", action="store_true")
    args = ap.parse_args()

    if args.smoke:
        args.N, args.seeds = 24, 2
        args.K_list = "3,6,12,24"
        args.student_steps = 150
        args.T, args.B_train, args.B_test = 30, 8, 12
        args.maxd = 10

    device = pick_device(args.device)
    K_list = [int(k) for k in args.K_list.split(",") if int(k) <= args.N]
    if args.N not in K_list:
        K_list.append(args.N)
    K_list = sorted(set(K_list))
    conditions = ["metric", "shuffle", "none"]
    if args.index_sanity:
        conditions.append("index")

    print(f"device={device}  N={args.N}  seeds={args.seeds}  K={K_list}  "
          f"conditions={conditions}  steps={args.student_steps}", flush=True)

    t0 = time.time()
    rows = []
    for s in range(args.seeds):
        print(f"=== seed {s+1}/{args.seeds} ===", flush=True)
        rows.append(run_seed(s, args.N, args.v, args.maxd, K_list, conditions,
                             device, args.student_steps, args.T,
                             args.B_train, args.B_test, args.g_rec, args.scale))

    agg, gaps = aggregate(rows, K_list, conditions)

    small_K = K_list[0]
    big_K = [k for k in K_list if k < args.N]
    big_K = big_K[-1] if big_K else K_list[-1]
    g_small, g_big = gaps.get(small_K, {}), gaps.get(big_K, {})
    shrinks = (g_big.get("metric_minus_shuffle_r2", 0.0)
               < g_small.get("metric_minus_shuffle_r2", 0.0))

    obs_metric = agg["metric"][small_K]["observed_r2_mean"]
    obs_shuffle = agg["shuffle"][small_K]["observed_r2_mean"]
    obs_matched = obs_metric <= obs_shuffle + 0.05

    idx_ok = True
    if "index" in conditions:
        di = abs(agg["metric"][small_K]["heldout_r2_mean"]
                 - agg["index"][small_K]["heldout_r2_mean"])
        idx_ok = bool(di < 0.3 * max(1e-6, abs(g_small.get("metric_minus_shuffle_r2", 0.0))) + 0.05)

    signal = (g_small.get("t", 0) > 3
              and g_small.get("wins", 0) >= max(1, math.ceil(0.8 * g_small.get("n", 1)))
              and g_small.get("metric_minus_shuffle_r2", 0) > 0
              and shrinks and obs_matched and idx_ok)
    verdict = ("SIGNAL (needs adversarial verify)" if signal
               else "marginal" if g_small.get("t", 0) > 1.5 else "null")

    res = {
        "design": "beiran-style delay identifiability: teacher distance-structured "
                  "delay-RNN; student learns a shared distance->weight kernel from K "
                  "recordings and predicts held-out units; metric vs entry-shuffle vs none",
        "params": vars(args), "device": device, "K_list": K_list,
        "conditions": conditions, "per_K": agg,
        "metric_minus_shuffle_gap_by_K": gaps,
        "small_K": small_K, "big_K": big_K,
        "gap_shrinks_with_K": bool(shrinks),
        "observed_fit_matched_at_smallK": bool(obs_matched),
        "index_sanity_ok": bool(idx_ok) if "index" in conditions else None,
        "verdict": verdict, "minutes": round((time.time() - t0) / 60, 2),
    }
    os.makedirs(os.path.dirname(args.out), exist_ok=True)
    with open(args.out, "w") as f:
        json.dump(res, f, indent=2)

    print("\n=== DELAY IDENTIFIABILITY VERDICT ===")
    for K in K_list:
        line = f"K={K:3d}  "
        for c in conditions:
            line += f"{c}:r2={agg[c][K]['heldout_r2_mean']:+.3f}  "
        if K in gaps:
            line += (f"| gap={gaps[K]['metric_minus_shuffle_r2']:+.3f} "
                     f"t={gaps[K]['t']:.2f} wins={gaps[K]['wins']}/{gaps[K]['n']}")
        print(line)
    print(f"observed-fit matched @smallK: {obs_matched} "
          f"(metric obs_r2={obs_metric:+.3f} vs shuffle {obs_shuffle:+.3f})")
    print(f"gap shrinks K->N: {shrinks}   index_sanity_ok: {res['index_sanity_ok']}")
    print("VERDICT:", verdict)
    print(f"wrote {args.out}  in {res['minutes']} min")


if __name__ == "__main__":
    main()
